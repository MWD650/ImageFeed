import UIKit

extension UIColor {
    static var ypBlack: UIColor {
        return UIColor(named: "ypBlack")!
    }
    static var ypWhite: UIColor {
        return UIColor(named: "ypWhite")!
    }
    static var ypRed: UIColor {
        return UIColor(named: "ypRed")!
    }
}

